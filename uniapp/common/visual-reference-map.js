export function findVisualReference(role, pageId) {
  const route = pageId.includes("/") ? pageId : `pages/${role}/${pageId}`;
  const source = role === "merchant" ? merchantVisualReferences : role === "guide" ? guideVisualReferences : userVisualReferences;
  return source.find((item) => item.route === route);
}
